    private void jButtonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveActionPerformed
        int selectedRow = jTableKeywords.getSelectedRow();
        if (selectedRow < 0) {
            return;
        }
        Integer selectedID = (Integer) Utils.getSelectedCellContent(selectedRow, 0, jTableKeywords);
        String sql = "DELETE FROM Ratingkeyword WHERE RatingkeywordID=?;";
        
        ArrayList<Object> data = new ArrayList<>();
        data.add(selectedID);
        
        DB.connect();
        DB.makeQuery(sql, data);
        loadKeywords();
        DB.close();
    }//GEN-LAST:event_jButtonRemoveActionPerformed

