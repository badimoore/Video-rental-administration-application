    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        jLabelNewKeyword.setForeground(Color.black);
        String newKeyword = jTextFieldNewKeyword.getText();
        
        if (newKeyword.isBlank()) {
            jLabelNewKeyword.setForeground(Color.red);
            return;
        }
        
        String sql = "INSERT INTO Ratingkeyword (RatingkeywordID, Keyword, Icon) VALUES (null, ?, null);";
        ArrayList<Object> data = new ArrayList<>();
        
        data.add(newKeyword);
        
        DB.connect();
        DB.makeQuery(sql, data);
        loadKeywords();
        DB.close();
    }//GEN-LAST:event_jButtonAddActionPerformed

