    private void jButtonAddPhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddPhotoActionPerformed
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Image files", "jpg", "gif", "png");
        chooser.addChoosableFileFilter(filter);
        chooser.setFileFilter(filter);
        chooser.setMultiSelectionEnabled(false);

        int choice = chooser.showOpenDialog(this);

        if (choice == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();

            try {
                jLabelImage.setIcon(new ImageIcon(file.getPath()));
                imageStream = new FileInputStream(file);

                jLabelImage.setText(file.getName());
            } catch (Exception e) {
                jLabelImage.setText("Error: Image failed to load");
            }
        }

    }//GEN-LAST:event_jButtonAddPhotoActionPerformed

