    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        jLabelFirstname.setForeground(Color.black);
        jLabelDOB.setForeground(Color.black);

        String firstname = jTextFieldFirstname.getText();
        String lastname = jTextFieldLastname.getText();
        String dobText = jTextFieldDOB.getText();
        String bio = jTextAreaBio.getText();

        //make sure that first name was entered. Last name is not required due to many artists only using a single name
        if (firstname.isEmpty()) {
            jTextFieldFirstname.setForeground(Color.red);
            return;
        }
        //make sure that date format is correct
        if (!dobText.isEmpty() && !dobText.matches("\\d\\d\\d\\d-\\d\\d-\\d\\d")) {
            jTextFieldDOB.setForeground(Color.red);
            return;
        }

        String sql;
        if (crewID > 0) {
            sql = "UPDATE Crew SET Firstname = ?, Lastname = ?, DOB = ?, Bio = ?";
            if (imageStream != null) {
                sql += ", Photo = ?";
            }
            sql += " WHERE CrewID = ?;";
            
        } else {
            sql = "INSERT INTO Crew (CrewID, Firstname, Lastname, DOB, Bio, Photo) VALUES (null, ?, ?, ?, ?, ?);";
        }
        ArrayList<Object> data = new ArrayList<>();

        //create array for data and fill it with values
        data.add(firstname);
        if (lastname.isEmpty()) {
            data.add(new NullParameter(Types.VARCHAR));
        } else {
            data.add(lastname);
        }
        if (dobText.isEmpty()) {
            data.add(new NullParameter(Types.DATE));
        } else {
            Date dob = Utils.getDate(dobText);
            data.add(dob);
        }
        if (bio.isEmpty()) {
            data.add(new NullParameter(Types.VARCHAR));
        } else {
            data.add(bio);
        }
        if (imageStream == null && crewID == 0) {
            data.add(new NullParameter(Types.BLOB));
        } else if (imageStream != null) {
            data.add(imageStream);
        }
        if (crewID > 0) {
            data.add(Integer.valueOf(crewID));
        }

        DB.connect();
        DB.makeQuery(sql, data);
        DB.close();

        closeWindow();
    }//GEN-LAST:event_jButtonSubmitActionPerformed

