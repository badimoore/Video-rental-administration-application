    public CrewInfo(DBManager DB, int crewID) {
        this.DB = DB;
        this.crewID = crewID;
        imageStream = null;

        initComponents();

        if (crewID != 0) {
            setTitle("Edit Cast/Crew");

            String sql = "SELECT Firstname, Lastname, DOB, Bio, Photo FROM Crew WHERE crewID = ?;";
            ArrayList<Object> data = new ArrayList<>();
            data.add(crewID);

            DB.connect();
            ResultSet result = DB.makeQuery(sql, data);
            DB.close();

            try {
                if (result.next()) {
                    jTextFieldFirstname.setText(result.getString(1));

                    String lastname = result.getString(2);
                    if (lastname != null) {
                        jTextFieldLastname.setText(lastname);
                    }

                    Date dob = result.getDate(3);
                    if (dob != null) {
                        jTextFieldDOB.setText(dob.toString());
                    }

                    String bio = result.getString(4);
                    if (bio != null) {
                        jTextAreaBio.setText(bio);
                    }

                    byte[] pic = result.getBytes(5);
                    if (pic != null) {
                        ImageIcon image = new ImageIcon(pic);
                        jLabelImage.setIcon(image);
                        jLabelImage.setText("Image loaded from database");
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error reading info", "Read error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
                closeWindow();
            }
        }
    }

